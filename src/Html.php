    public static function tag(string $name, string|Stringable $content = '', array $attributes = []): CustomTag
    {
        $tag = CustomTag::name($name);
        if ($content !== '') {
            $tag = $tag->content($content);
        }
        if (!empty($attributes)) {
            $tag = $tag->attributes($attributes);
        }
        return $tag;
    }

